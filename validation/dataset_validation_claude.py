"""
Dataset Validation with Claude as Judge
=========================================
Independently re-annotates each anomalous image using Claude Sonnet 4.6,
then compares against GPT-4o-generated GT annotations.

For each image, asks Claude to:
  1. Classify the anomaly into one of 11 classes (independent, not shown GT class)
  2. Rate the GPT-4o description's factuality (1-5) against the actual image
  3. Rate completeness (1-5)
  4. Flag any inaccuracies

Cost optimisation:
  - Images resized to 768 px longest side (cuts image tokens ~4×)
  - Prompt caching on the system prompt (~80% savings on cached reads)
  - Brief structured JSON output

Output:
  Data/validation/claude_validation.json   – per-image results (incremental)
  Data/validation/summary.json             – Cohen's kappa, agreement stats

Usage:
  export ANTHROPIC_API_KEY=sk-ant-...
  python dataset_validation_claude.py --limit 100   # initial test
  python dataset_validation_claude.py --limit 5000  # full run after approval
"""

import argparse
import base64
import io
import json
import os
import sys
import time
from pathlib import Path

from PIL import Image
from tqdm import tqdm

import anthropic

# ── Config ────────────────────────────────────────────────────────────────────
ROOT       = Path(__file__).resolve().parent.parent
IMG_DIR    = ROOT / "Data" / "images"
DS_JSON    = ROOT / "Data" / "dataset.json"
OUT_DIR    = ROOT / "Data" / "validation"
OUT_FILE   = OUT_DIR / "claude_validation.json"
SUMMARY    = OUT_DIR / "summary.json"
DISAGREE   = OUT_DIR / "disagreements.json"
DISAGREE_CSV = OUT_DIR / "disagreements_for_review.csv"

OUT_DIR.mkdir(parents=True, exist_ok=True)

MODEL_ID    = "claude-sonnet-4-6"   # Claude Sonnet 4.6
IMG_MAX_SIDE = 768                  # resize longest side to this
MAX_TOKENS  = 350                   # brief structured output

ANOMALY_CLASSES = [
    "animal_on_road",
    "extreme_weather",
    "road_surface_hazard",
    "fallen_debris_or_vegetation",
    "strange_object_on_road",
    "vehicle_incident",
    "infrastructure_failure",
    "human_presence_anomaly",
    "adverse_lighting",
    "oversized_or_unusual_vehicle",
    "multi_hazard_compound",
]

SYSTEM_PROMPT = """You are an expert independent annotator for an autonomous \
driving anomaly-detection benchmark. You will be shown a dashcam image and a \
candidate description, and asked to (a) classify the primary anomaly visible \
and (b) rate the description's accuracy against what you actually see.

ANOMALY CLASSES (memorise; respond with one of these exact strings):
1. animal_on_road — animal physically ON the road or crossing in front of the \
ego vehicle (deer, dog, cow, horse, wild boar). Animals on the sidewalk DO NOT count.
2. extreme_weather — severe weather making the road invisible/impassable: \
dense fog, active flooding, heavy blizzard, black ice. Light rain / overcast \
DO NOT count.
3. road_surface_hazard — damage IN the road surface: large potholes, \
sinkholes, collapsed pavement, rockslides across the road.
4. fallen_debris_or_vegetation — fallen branches/trees/leaves blocking the road.
5. strange_object_on_road — unusual objects on the road (furniture, tires, \
appliances, mattresses).
6. vehicle_incident — vehicle accident, crash, collision, vehicle on its side, \
emergency stop with debris.
7. infrastructure_failure — damaged road infrastructure: fallen traffic signs, \
broken barriers, downed utility poles.
8. human_presence_anomaly — pedestrian/person walking on a highway or in an \
unsafe area where pedestrians shouldn't be.
9. adverse_lighting — extreme lighting issues: blinding glare, severe headlight \
flare, near-total darkness with no visibility. Normal night driving DOES NOT count.
10. oversized_or_unusual_vehicle — oversized or unusual vehicle: large trucks, \
construction vehicles, trailers, military vehicles, agricultural equipment.
11. multi_hazard_compound — two or more distinct hazards simultaneously.

You MUST respond with only valid JSON — no preamble, no code fences, no markdown. \
Schema:
{
  "your_class": "<one of the 11 strings above>",
  "class_confidence": <float 0.0-1.0>,
  "description_factuality": <int 1-5>,
  "description_completeness": <int 1-5>,
  "agrees_with_description": <true|false>,
  "issues": "<short string, or empty>"
}

Rating rubric:
- factuality: 5 = perfectly accurate; 4 = mostly accurate, minor inaccuracy; \
3 = roughly accurate but contains errors; 2 = mostly inaccurate; 1 = fabricated.
- completeness: 5 = captures all major visible elements; 3 = captures the main \
anomaly only; 1 = misses the primary anomaly.

Be strict. If the description is wrong, say so.
"""

USER_TEMPLATE = ("Candidate description (generated by another AI): \"{description}\"\n\n"
                 "Independently classify the primary anomaly visible in the image, "
                 "then rate the candidate description against what you actually see. "
                 "Respond with JSON only.")


# ── Image preprocessing ───────────────────────────────────────────────────────
def load_resized_image_b64(path: Path, max_side: int = IMG_MAX_SIDE) -> str:
    """Open image, resize so longest side <= max_side, encode as base64 JPEG."""
    img = Image.open(path).convert("RGB")
    w, h = img.size
    if max(w, h) > max_side:
        scale = max_side / max(w, h)
        img   = img.resize((int(w * scale), int(h * scale)), Image.LANCZOS)
    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=85)
    return base64.b64encode(buf.getvalue()).decode("utf-8")


# ── Load GT ───────────────────────────────────────────────────────────────────
def load_anomalous_gt():
    with open(DS_JSON) as f:
        data = json.load(f)
    samples = data.get("samples", data)
    rows = []
    for fname, rec in samples.items():
        if not isinstance(rec, dict):
            continue
        if not rec.get("anomaly_present"):
            continue
        ac = rec.get("anomaly_class")
        if ac not in ANOMALY_CLASSES:
            continue
        rows.append({
            "filename":          fname,
            "gt_class":          ac,
            "gt_description":    rec.get("anomaly_description", ""),
            "gt_severity":       rec.get("severity", ""),
            "gt_key_facts":      rec.get("anomaly_key_facts", []),
            "image_id":          rec.get("image_id", ""),
        })
    rows.sort(key=lambda r: r["filename"])  # deterministic order
    return rows


# ── Claude call ───────────────────────────────────────────────────────────────
def judge_one(client, gt_row):
    img_b64 = load_resized_image_b64(IMG_DIR / gt_row["filename"])

    msg = client.messages.create(
        model=MODEL_ID,
        max_tokens=MAX_TOKENS,
        system=[
            {
                "type":          "text",
                "text":          SYSTEM_PROMPT,
                "cache_control": {"type": "ephemeral"},   # 5-min prompt cache
            }
        ],
        messages=[
            {
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "source": {
                            "type":       "base64",
                            "media_type": "image/jpeg",
                            "data":       img_b64,
                        },
                    },
                    {
                        "type": "text",
                        "text": USER_TEMPLATE.format(
                            description=gt_row["gt_description"][:1200] or "(none provided)"
                        ),
                    },
                ],
            }
        ],
    )

    # Parse Claude's JSON output
    text = msg.content[0].text.strip()
    # Strip code fences if Claude added them despite instructions
    if text.startswith("```"):
        text = text.split("```")[1]
        if text.startswith("json"):
            text = text[4:]
        text = text.strip()
    try:
        parsed = json.loads(text)
    except json.JSONDecodeError as e:
        parsed = {"_parse_error": str(e), "_raw": text}

    return {
        "filename":          gt_row["filename"],
        "gt_class":          gt_row["gt_class"],
        "gt_description":    gt_row["gt_description"],
        "claude_response":   parsed,
        "usage": {
            "input_tokens":               msg.usage.input_tokens,
            "output_tokens":              msg.usage.output_tokens,
            "cache_creation_input_tokens": getattr(msg.usage, "cache_creation_input_tokens", 0),
            "cache_read_input_tokens":     getattr(msg.usage, "cache_read_input_tokens", 0),
        },
    }


# ── Cost estimator ────────────────────────────────────────────────────────────
def estimate_cost(usage_records, model=MODEL_ID):
    """Returns running cost in USD given list of usage dicts."""
    INPUT_RATE       = 3.00 / 1_000_000   # $/token
    OUTPUT_RATE      = 15.00 / 1_000_000
    CACHE_WRITE_RATE = 3.75 / 1_000_000   # 25% premium
    CACHE_READ_RATE  = 0.30 / 1_000_000   # 90% discount

    total = 0.0
    for u in usage_records:
        total += u["input_tokens"]                * INPUT_RATE
        total += u["output_tokens"]               * OUTPUT_RATE
        total += u["cache_creation_input_tokens"] * CACHE_WRITE_RATE
        total += u["cache_read_input_tokens"]     * CACHE_READ_RATE
    return total


# ── Summary stats ─────────────────────────────────────────────────────────────
def compute_summary(records):
    """Compute agreement metrics between Claude and GPT-4o."""
    valid = [r for r in records if isinstance(r["claude_response"], dict)
             and "your_class" in r["claude_response"]]
    n = len(valid)
    if n == 0:
        return {"n_valid": 0}

    y_gt     = [r["gt_class"] for r in valid]
    y_claude = [r["claude_response"]["your_class"] for r in valid]

    n_agree = sum(1 for a, b in zip(y_gt, y_claude) if a == b)
    raw_agreement = n_agree / n

    # Cohen's kappa
    try:
        from sklearn.metrics import cohen_kappa_score
        kappa = float(cohen_kappa_score(y_gt, y_claude, labels=ANOMALY_CLASSES))
    except Exception:
        kappa = None

    # Per-class agreement
    per_class = {}
    for c in ANOMALY_CLASSES:
        n_c       = sum(1 for g in y_gt if g == c)
        n_agree_c = sum(1 for g, p in zip(y_gt, y_claude) if g == c and p == c)
        per_class[c] = {
            "n_in_gt":      n_c,
            "claude_match": n_agree_c,
            "match_rate":   round(n_agree_c / n_c, 3) if n_c else None,
        }

    # Description ratings
    facts = [r["claude_response"].get("description_factuality")    for r in valid]
    facts = [f for f in facts if isinstance(f, (int, float))]
    comps = [r["claude_response"].get("description_completeness")  for r in valid]
    comps = [c for c in comps if isinstance(c, (int, float))]

    return {
        "n_total":                  len(records),
        "n_valid":                  n,
        "raw_class_agreement":      round(raw_agreement, 4),
        "cohens_kappa":             round(kappa, 4) if kappa is not None else None,
        "per_class_agreement":      per_class,
        "mean_factuality":          round(sum(facts) / len(facts), 3) if facts else None,
        "mean_completeness":        round(sum(comps) / len(comps), 3) if comps else None,
        "n_agrees_with_description": sum(1 for r in valid
                                         if r["claude_response"].get("agrees_with_description") is True),
    }


# ── Disagreement extraction ───────────────────────────────────────────────────
def extract_disagreements(records):
    """
    Identify cases where Claude disagrees with GPT-4o.
    Two kinds of disagreement:
      1. CLASS disagreement: Claude's class != GPT-4o's class
      2. DESCRIPTION concern: factuality <= 3 or agrees_with_description == False
    """
    disagreements = []
    for r in records:
        cr = r.get("claude_response", {})
        if not isinstance(cr, dict) or "your_class" not in cr:
            continue

        claude_class = cr.get("your_class")
        gt_class     = r.get("gt_class")
        factuality   = cr.get("description_factuality")
        completeness = cr.get("description_completeness")
        agrees_desc  = cr.get("agrees_with_description")
        issues       = cr.get("issues", "")
        confidence   = cr.get("class_confidence")

        class_disagree = (claude_class != gt_class)
        desc_concern   = ((isinstance(factuality, (int, float)) and factuality <= 3)
                          or (agrees_desc is False))

        if class_disagree or desc_concern:
            reason = []
            if class_disagree:
                reason.append(f"class: GPT={gt_class} vs Claude={claude_class}")
            if desc_concern:
                reason.append(f"description (factuality={factuality}, agrees={agrees_desc})")

            disagreements.append({
                "filename":              r["filename"],
                "image_path":            f"Data/images/{r['filename']}",
                "disagreement_type":     "class+desc" if (class_disagree and desc_concern) else
                                         "class" if class_disagree else "description",
                "reason":                "; ".join(reason),
                "gt_class":              gt_class,
                "claude_class":          claude_class,
                "claude_class_confidence": confidence,
                "gt_description":        r.get("gt_description", ""),
                "claude_factuality":     factuality,
                "claude_completeness":   completeness,
                "claude_agrees_with_desc": agrees_desc,
                "claude_issues":         issues,
            })
    return disagreements


def save_disagreements_csv(disagreements, path):
    """Save a flat CSV that's easy to scan in Excel / a viewer."""
    import csv
    if not disagreements:
        return
    fieldnames = ["filename", "image_path", "disagreement_type", "reason",
                  "gt_class", "claude_class", "claude_class_confidence",
                  "claude_factuality", "claude_completeness",
                  "claude_agrees_with_desc", "claude_issues",
                  "gt_description"]
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for d in disagreements:
            writer.writerow({k: d.get(k, "") for k in fieldnames})


# ── Main ──────────────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--limit",       type=int, default=100,
                        help="How many anomalous images to validate (default 100)")
    parser.add_argument("--start",       type=int, default=0,
                        help="Start index into the sorted anomalous-image list")
    parser.add_argument("--api-key-env", default="ANTHROPIC_API_KEY")
    args = parser.parse_args()

    api_key = os.environ.get(args.api_key_env)
    if not api_key:
        sys.exit(f"[ERROR] Set ${args.api_key_env}")

    client = anthropic.Anthropic(api_key=api_key)
    print(f"[INFO] Model: {MODEL_ID}")

    rows = load_anomalous_gt()
    print(f"[INFO] Anomalous images: {len(rows):,}")

    target = rows[args.start : args.start + args.limit]
    print(f"[INFO] Processing index range [{args.start}, {args.start + args.limit}), "
          f"{len(target)} images")

    # Resume support
    done = {}
    if OUT_FILE.exists():
        with open(OUT_FILE) as f:
            existing = json.load(f)
        for r in existing.get("records", []):
            done[r["filename"]] = r
        print(f"[INFO] Resuming: {len(done)} already validated")

    todo = [r for r in target if r["filename"] not in done]
    print(f"[INFO] To process now: {len(todo)}")

    records = list(done.values())
    usage_records = []

    for gt_row in tqdm(todo, desc="Claude judge", unit="img"):
        for attempt in range(3):
            try:
                result = judge_one(client, gt_row)
                records.append(result)
                usage_records.append(result["usage"])
                break
            except anthropic.RateLimitError:
                wait = 5 * (attempt + 1)
                print(f"  [Rate limit] sleeping {wait}s")
                time.sleep(wait)
            except Exception as e:
                print(f"  [WARN] {gt_row['filename']}: {e}")
                if attempt == 2:
                    records.append({
                        "filename":   gt_row["filename"],
                        "gt_class":   gt_row["gt_class"],
                        "claude_response": {"_error": str(e)},
                    })
                else:
                    time.sleep(2)

        # Save incrementally every 10 calls
        if len(records) % 10 == 0:
            with open(OUT_FILE, "w") as f:
                json.dump({"model": MODEL_ID, "records": records}, f, indent=2)

    # Final save + summary
    with open(OUT_FILE, "w") as f:
        json.dump({"model": MODEL_ID, "records": records}, f, indent=2)

    summary = compute_summary(records)
    cost    = estimate_cost(usage_records)
    summary["cost_this_run_usd"] = round(cost, 4)
    summary["n_calls_this_run"]  = len(usage_records)

    # Extract disagreements for human review
    disagreements = extract_disagreements(records)
    n_class_only = sum(1 for d in disagreements if d["disagreement_type"] == "class")
    n_desc_only  = sum(1 for d in disagreements if d["disagreement_type"] == "description")
    n_both       = sum(1 for d in disagreements if d["disagreement_type"] == "class+desc")

    summary["n_disagreements"]            = len(disagreements)
    summary["disagreements_class_only"]   = n_class_only
    summary["disagreements_desc_only"]    = n_desc_only
    summary["disagreements_both"]         = n_both
    summary["disagreement_rate"]          = round(len(disagreements) /
                                                  max(summary.get("n_valid", 1), 1), 4)

    with open(SUMMARY, "w") as f:
        json.dump(summary, f, indent=2)
    with open(DISAGREE, "w") as f:
        json.dump({"model": MODEL_ID, "n_disagreements": len(disagreements),
                   "disagreements": disagreements}, f, indent=2)
    save_disagreements_csv(disagreements, DISAGREE_CSV)

    print(f"\n{'='*60}")
    print(f"  Dataset Validation Summary")
    print(f"{'='*60}")
    print(f"  Validated          : {summary.get('n_valid', 0)} / {summary.get('n_total', 0)}")
    print(f"  Raw agreement      : {summary.get('raw_class_agreement')}")
    print(f"  Cohen's kappa      : {summary.get('cohens_kappa')}")
    print(f"  Mean factuality    : {summary.get('mean_factuality')}")
    print(f"  Mean completeness  : {summary.get('mean_completeness')}")
    print(f"  Cost this run      : ${cost:.4f}")
    print(f"")
    print(f"  Disagreements      : {len(disagreements)} "
          f"({summary['disagreement_rate']:.1%} of validated)")
    print(f"    class-only       : {n_class_only}")
    print(f"    description-only : {n_desc_only}")
    print(f"    both             : {n_both}")
    print(f"")
    print(f"  Outputs:")
    print(f"    {OUT_FILE}")
    print(f"    {SUMMARY}")
    print(f"    {DISAGREE}              — full detail for review")
    print(f"    {DISAGREE_CSV}  — open in Excel / viewer")


if __name__ == "__main__":
    main()
